#include <math.h>
#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char* readline();

char* timeConversion(char* s) {
    static char result[9]; // hh:mm:ss format (8 chars + null terminator)
    int hour, minute, second;
    char ampm[3]; // "AM" or "PM"

    // Parse the input string
    sscanf(s, "%2d:%2d:%2d%2s", &hour, &minute, &second, ampm);

    // Convert to 24-hour format
    if (strcmp(ampm, "AM") == 0) {
        if (hour == 12) hour = 0; // Midnight case
    } else { // PM
        if (hour != 12) hour += 12; // Afternoon case
    }

    // Format into result string
    sprintf(result, "%02d:%02d:%02d", hour, minute, second);

    return result;
}


int main()
{
    FILE* fptr = fopen(getenv("OUTPUT_PATH"), "w");

    char* s = readline();

    char* result = timeConversion(s);

    fprintf(fptr, "%s\n", result);

    fclose(fptr);

    return 0;
}

char* readline() {
    size_t alloc_length = 1024;
    size_t data_length = 0;

    char* data = malloc(alloc_length);

    while (true) {
        char* cursor = data + data_length;
        char* line = fgets(cursor, alloc_length - data_length, stdin);

        if (!line) {
            break;
        }

        data_length += strlen(cursor);

        if (data_length < alloc_length - 1 || data[data_length - 1] == '\n') {
            break;
        }

        alloc_length <<= 1;

        data = realloc(data, alloc_length);

        if (!data) {
            data = '\0';

            break;
        }
    }

    if (data[data_length - 1] == '\n') {
        data[data_length - 1] = '\0';

        data = realloc(data, data_length);

        if (!data) {
            data = '\0';
        }
    } else {
        data = realloc(data, data_length + 1);

        if (!data) {
            data = '\0';
        } else {
            data[data_length] = '\0';
        }
    }

    return data;
}
